
export function getSuccessAlert(msg) {
    return ("<div class='alert alert-success'>"
        + msg +
        "</div>");
}

export function getDangerAlert(msg) {
    return "<div class='alert alert-danger'>"
        + msg +
        "</div>";
}

export function getWarningAlert(msg) {
    return ("<div class='alert alert-warning'>"
        + msg +
        "</div>");
}
